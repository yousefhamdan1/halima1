package rev1;

public class rap67 {

        public static void main(String[] args) {
            int n = 10; // number of columns
            for (int i = 1; i <= 5; i++) {
                for (int j = 1; j <= n; j++) {
                    System.out.print(i * j + " ");
                }
                System.out.println();
            }
        }
    }

